#!/usr/bin/env python3
"""cascade_calibration.py — D5, downscoped to what the recorded data supports.

Loads the recorded verdict sets and reports: per-tier accept/reject agreement,
flip counts, sub-label noise, and the honest conclusion. NO invented token
volumes, NO swept thresholds over constant data (spec AC-D1: the recorded runs
carry zero accept/reject flips and no token counts — a confidence-based cascade
can be neither justified nor refuted from this data).

Usage: python3 evals/cascade_calibration.py [--write PATH]
"""
import json
import os
import sys

EVALS = os.path.dirname(os.path.abspath(__file__))
BASELINE = os.path.join(EVALS, "baselines", "verifier_verdicts.json")
HARD = os.path.join(EVALS, "cases", "hard", "recorded_verdicts.json")

ACCEPT = ("PASS",)


def _ar(v):
    return "accept" if v in ACCEPT else "reject"


def _pairs(run_a, run_b):
    """Common-id verdict pairs between two runs (dicts id->verdict)."""
    common = sorted(set(run_a) & set(run_b))
    return [(i, run_a[i], run_b[i]) for i in common]


def _run_map(rec):
    out = {}
    for x in rec:
        if isinstance(x, dict) and "id" in x and "verdict" in x:
            out[x["id"]] = x["verdict"]
    return out


def analyze():
    hard = json.load(open(HARD, encoding="utf-8"))
    baseline = json.load(open(BASELINE, encoding="utf-8"))
    report = {"data": {"baseline_gold_n": len(baseline)}}
    tiers = {}
    for tier, (a_key, b_key) in {"sonnet": ("v1_run_A", "v1_run_B"),
                                 "haiku": ("haiku_run_A", "haiku_run_B")}.items():
        if a_key not in hard or b_key not in hard:
            continue
        a, b = _run_map(hard[a_key]), _run_map(hard[b_key])
        pairs = _pairs(a, b)
        flips = [i for (i, va, vb) in pairs if _ar(va) != _ar(vb)]
        sub = [i for (i, va, vb) in pairs if va != vb and _ar(va) == _ar(vb)]
        tiers[tier] = {"n_common": len(pairs),
                       "accept_reject_flips": len(flips),
                       "flip_ids": flips,
                       "sublabel_disagreements": len(sub),
                       "sublabel_rate": round(len(sub) / len(pairs), 3) if pairs else None}
    report["tiers"] = tiers

    # cross-tier agreement on the ids both tiers judged
    if "sonnet" in tiers and "haiku" in tiers:
        s = _run_map(hard["v1_run_A"]); h = _run_map(hard["haiku_run_A"])
        pairs = _pairs(s, h)
        cross_flips = [i for (i, vs, vh) in pairs if _ar(vs) != _ar(vh)]
        report["cross_tier"] = {"n_common": len(pairs),
                                "accept_reject_disagreements": len(cross_flips),
                                "ids": cross_flips}

    zero_flips = all(t["accept_reject_flips"] == 0 for t in tiers.values())
    report["conclusion"] = {
        "saturated": zero_flips,
        "cascade_verdict": "DEFER — insufficient data",
        "reason": ("Both tiers show zero accept/reject flips across repeated runs on "
                   "the recorded corpus (saturation), the certified baseline is a "
                   "single merged run, and no per-run token counts were recorded. A "
                   "confidence-based Haiku->Sonnet cascade can be neither justified "
                   "nor refuted from this data."),
        "prerequisites": ["non-saturated execution-grounded or fault-injected cases "
                          "(backlog D1)",
                          "per-run token usage recorded (runner/run_trace.py "
                          "DEFAULT_RATES ready as cost weights)"],
    }
    return report


def render(report):
    lines = ["# D5 cascade calibration — honest report (2026-07-01)", ""]
    for tier, t in report["tiers"].items():
        lines.append("- **%s**: %d common cases, %d accept/reject flips, "
                     "%d sub-label (FAIL<->FALSE-PASS) disagreements (rate %s)"
                     % (tier, t["n_common"], t["accept_reject_flips"],
                        t["sublabel_disagreements"], t["sublabel_rate"]))
    if "cross_tier" in report:
        c = report["cross_tier"]
        lines.append("- **cross-tier (sonnet vs haiku, run A)**: %d common, %d "
                     "accept/reject disagreements" % (c["n_common"],
                                                      c["accept_reject_disagreements"]))
    con = report["conclusion"]
    lines += ["", "## Verdict: %s" % con["cascade_verdict"], "", con["reason"], "",
              "Prerequisites before this question is answerable:"]
    lines += ["- %s" % p for p in con["prerequisites"]]
    lines += ["", "_Generated by evals/cascade_calibration.py — deterministic, no "
              "API calls, no invented token volumes._"]
    return "\n".join(lines)


def main():
    report = analyze()
    out = render(report)
    print(json.dumps(report, indent=1))
    if "--write" in sys.argv:
        path = sys.argv[sys.argv.index("--write") + 1]
        open(path, "w", encoding="utf-8").write(out + "\n")
    return 0


if __name__ == "__main__":
    sys.exit(main())
